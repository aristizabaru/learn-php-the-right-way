<?php // Expresiones en PHP

/**
 * Expresiones en PHP
 * La expresiones son los bloques de construcción más importantes de PHP.
 * En PHP casi todo lo que se escribe es una expresión. La manera más
 * simple y acertada de definir lo que es una expresión es
 * «cualquier cosa que tiene un valor o evalua a un valor».
 * 
 * PHP es un lenguaje orientado a expresiones
 * 
 * Documentación: https://www.php.net/manual/es/language.expressions.php
 */

// Expresiones

$x = 5; // expresión que evalua a 5.

$x = $y;

$z = $x === $y; // expresión que evalua a true(1)
