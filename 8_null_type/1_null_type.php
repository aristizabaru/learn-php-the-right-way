<?php // Null Type

/**
 * Null Type
 * El valor especial null representa una variable sin valor.
 * null es el único valor posible del tipo null
 * 
 * Documentación oficial :https://www.php.net/manual/es/language.types.null.php
 */

// null constant
$x = null;
var_dump($x);

// la función incorporada is_null permite validar si el dato es null
echo is_null($x);
